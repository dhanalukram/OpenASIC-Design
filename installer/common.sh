#!/usr/bin/env bash

###############################################################################
# OpenASIC-Design
# Common Utility Functions
###############################################################################

PROJECT_NAME="OpenASIC-Design"
VERSION="0.1.0"

###############################################################################
# Colors
###############################################################################

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

###############################################################################
# Printing Functions
###############################################################################

print_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

print_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

###############################################################################
# Project Banner
###############################################################################

show_title() {
    echo
    echo "=============================================================="
    echo "                     OpenASIC-Design"
    echo "      Open-Source ASIC Design Environment"
    echo "                     Version ${VERSION}"
    echo "=============================================================="
    echo
}
